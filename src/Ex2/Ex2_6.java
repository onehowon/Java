
public class Ex2_6 {
	public static void main(String args[]) {
		int x = 100; // 정수를 저장할 변수 타입은 Int
		double pi = 3.14; // 실수를 저장할 변수 타입은 do
		char ch = 'a'; // 문자를 저장할 변수의 타입은 char
		String str = "abc"; // 여러 문자를 저장할 변수의 타입은 String
		
		System.out.println(x);
		System.out.println(pi);
		System.out.println(ch);
		System.out.println(str);
	}

}
